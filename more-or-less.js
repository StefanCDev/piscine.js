


// declaring a constant variable called "more" where n is the argument 
// (results)=> tells us this is a function declaration 
//and apply the operation "+" the number in this case "1"
const more = (n) => n + 1
console.log(more(1))


// same operation but in reverse
const less = (n) => n -1
console.log(less(2))

// in this situation function takes two arguments and adds the together 
// make sure in console.log you have two valuee
const add = (n, b) => n + b
console.log(add(1, 2))

// same as previous example but in reverse
const sub = (n, b) => n - b
console.log(sub(1, 2))

